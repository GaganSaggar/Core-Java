import java.util.Scanner;
class Student
{
 String name;
 Scanner sc = new Scanner(System.in);
 void getdata()
 {
  System.out.println("Enter the name of the student");
  name = sc.next();
 }
}
class Marks extends Student
{
 static int M[] = new int[5];
 int i;
 Scanner sc = new Scanner(System.in);
 void getmarks()
 {
  System.out.println("Enter the Five Subjects marks");
  for(i=0;i<5;i++)
  {
   M[i] = sc.nextInt();
  }
 }
}
class Result extends Marks
{
 public static void main(String[] args)
 {
  int s=0,i;
  float per;
  Scanner sc = new Scanner(System.in);
  Result r = new Result();
  System.out.println("Program of Multilevel Inheritance");
  r.getdata();
  r.getmarks();
  for(i=0;i<5;i++)
  {
   s = s+M[i];
  }
  per = s/5;
  System.out.println("Total Marks = "+s);
  System.out.println("Percentage = "+per);
 }
}



































































































































































































































































































































































































































































































































































































































































































































































































































































































